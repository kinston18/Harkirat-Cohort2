// Assuming gender is represented as 'male' or 'female'
let gender = 'female'; // You can change this to 'male' for a different greeting

if (gender === 'male') {
  console.log('Hello, Sir!'); // Greeting for males
} else if (gender === 'female') {
  console.log('Hello, Madam!'); // Greeting for females
} else {
  console.log('Hello!'); // Default greeting if gender is not specified or invalid
}
